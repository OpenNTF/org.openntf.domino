package org.openntf.domino.graph2.exception;

public class ElementKeyException extends GraphException {

	public ElementKeyException(final String arg0) {
		super(arg0);
	}

	public ElementKeyException(final Throwable arg0) {
		super(arg0);
	}

	public ElementKeyException(final String arg0, final Throwable arg1) {
		super(arg0, arg1);
	}

}
